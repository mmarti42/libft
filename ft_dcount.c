#include <string.h>

size_t	ft_dcount(int nb)
{
	int count;

	count = 0;
	if (nb < 0)
		count++;
	if (nb >= 0 && nb < 10)
		return (1);
	while (nb != 0)
	{
		nb /= 10;
		count++;
	}
	return (count);
}
