char	*ft_strrev(char *s)
{
	int		i;
	int		j;
	char	c;

	if (!s)
		return (0);
	i = 0;
	j = 0;
	while (s[j] != 0)
		j++;
	j--;
	while (i < j)
	{
		c = s[i];
		s[i] = s[j];
		s[j] = c;
		j--;
		i++;
	}
	return (s);
}
