int ft_pow2(int pow)
{
	int res;

	res = 1;
	if (pow == 0)
		return (1);
	if (pow > 0)
	{
		while (pow != 0)
		{
			res *= 2;
			pow--;
		}
	}
	else
	{
		while (pow != 0)
		{
			res *= 2;
			pow++;
		}
	}
	return (res);
}